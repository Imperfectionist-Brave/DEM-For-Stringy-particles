	bool Law2_ScGeom6D_NodeFrictPhys_MembraneLaw::go(shared_ptr<IGeom>& ig, shared_ptr<IPhys>& ip, Interaction* contact)
	{
		//LOG_TRACE("Law2_ScGeom_WirePhys_WirePM::go - contact law");

		ScGeom6D*     geom = YADE_CAST<ScGeom6D*>(ig.get());
		NodeFrictPhys* phys = YADE_CAST<NodeFrictPhys*>(ip.get());

		//const Real&   dt = scene->dt;
		const int&    id1 = contact->getId1();
		const int&    id2 = contact->getId2();
		//Body*      b1 = Body::byId(id1, scene).get();
		//Body*      b2 = Body::byId(id2, scene).get();
		//Vector3r&     shearForceFirst = phys->shearForce;
		//if (contact->isFresh(scene)) shearForceFirst = Vector3r::Zero();
		Real un = geom->penetrationDepth;//穿透深度
		Real D = un - phys->initD;//位移变化量un<初始 D>0 受压 当前穿透深度-初始穿透深度  D>0 受压
		Real Fn = 0;

		State* st1 = Body::byId(id1, scene)->state.get();
		State* st2 = Body::byId(id2, scene)->state.get();
		Vector3r&       shearForce = geom->rotate(phys->shearForce);//变化位矢
		const Vector3r& dus = geom->shearIncrement();

		if (useK) {
			Fn = phys->Kn*D*geom->normal.norm();
			shearForce -= phys->Ks * dus;//Ks计算切向力
			if ((D < 0) && (-Fn > phys->normalLimit)) return false;//断裂
		}
		else {
			shearForce -= phys->ks * dus;//ks计算切向力
			if (un > 0) Fn = phys->kn*un*geom->normal.norm();
			else Fn = phys->Kn*D*geom->normal.norm();
		}
		phys->normalForce = Fn * geom->normal;//法向力

		//Apply the force
		applyForceAtContactPoint(
			-phys->normalForce - shearForce,
			geom->contactPoint,
			id1,
			st1->se3.position,
			id2,
			st2->se3.position + (scene->isPeriodic ? scene->cell->intrShiftPos(contact->cellDist) : Vector3r::Zero()));

		return true;
	}
