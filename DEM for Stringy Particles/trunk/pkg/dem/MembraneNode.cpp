	void Bo1_MembraneNode_Aabb::go(const shared_ptr<Shape>& cm, shared_ptr<Bound>& bv, const Se3r& se3, const Body* /*b*/)
	{
		MembraneNode* MN = static_cast<MembraneNode*>(cm.get());
		if (!bv) { bv = shared_ptr<Bound>(new Aabb); }
		Aabb*    aabb = static_cast<Aabb*>(bv.get());
		Vector3r halfSize = (aabbEnlargeFactor > 0 ? aabbEnlargeFactor : 1.) * Vector3r(MN->radius, MN->radius, MN->radius);
		if (!scene->isPeriodic) {
			aabb->min = se3.position - halfSize;
			aabb->max = se3.position + halfSize;
			return;
		}
	}
